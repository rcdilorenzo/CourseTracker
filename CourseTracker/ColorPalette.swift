import UIKit

func primaryColor() -> UIColor {
    return UIColor(red:0.47, green:0.69, blue:0.49, alpha:1)
}

func secondaryColor() -> UIColor {
    return UIColor(red:0.27, green:0.31, blue:0.29, alpha:1)
}